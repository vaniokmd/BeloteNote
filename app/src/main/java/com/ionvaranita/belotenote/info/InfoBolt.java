package com.ionvaranita.belotenote.info;

public class InfoBolt {
    private Integer idGioco;
    private Integer idPartida;
    private Integer idPersona;
    private Integer nrBolt;
    private Integer idRiga;

    public Integer getIdGioco() {
        return idGioco;
    }

    public void setIdGioco(Integer idGioco) {
        this.idGioco = idGioco;
    }

    public Integer getIdPartida() {
        return idPartida;
    }

    public void setIdPartida(Integer idPartida) {
        this.idPartida = idPartida;
    }

    public Integer getIdPersona() {
        return idPersona;
    }

    public void setIdPersona(Integer idPersona) {
        this.idPersona = idPersona;
    }

    public Integer getNrBolt() {
        return nrBolt;
    }

    public void setNrBolt(Integer nrBolt) {
        this.nrBolt = nrBolt;
    }

    public Integer getIdRiga() {
        return idRiga;
    }

    public void setIdRiga(Integer idRiga) {
        this.idRiga = idRiga;
    }
}
